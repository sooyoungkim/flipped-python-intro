# (1) Vector addition
# [2]   [2]   [3]   [ 7]
#     +     +     =
# [2]   [3]   [5]   [10]
########################################################
u = [2, 2]
v = [2, 3]
z = [3, 5]

# [(2, 2, 3), (2, 3, 5)] -> [7, 10]
result = [sum(t) for t in zip(u, v, z)]
print(result)

